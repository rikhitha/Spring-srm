package springiocex;

public class Empdesignation {

	private String empdesig;
	
	public String getEmpdesig() {
		return empdesig;
	}

	public void setEmpdesig(String empdesig) {
		this.empdesig = empdesig;
	}
	@Override
	public String toString() {
		return "Empdesignation [empdesig=" + empdesig + "]";
	}

}
